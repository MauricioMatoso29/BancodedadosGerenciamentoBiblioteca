    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // entidades:
        Usuario usu=new Usuario();
        Livro liv = new Livro();
        Exemplar exem = new Exemplar();
        Funcionario f= new Funcionario();        
        Emprestimo emp =new Emprestimo();        
        //classes para trabalhar com datas:
        Calendar cal =Calendar.getInstance();        
        SimpleDateFormat adf = new SimpleDateFormat("yyyy/MM/dd");//formato que eu uero usar     
        Calendar cal2 =Calendar.getInstance();
        cal2.add(Calendar.MONTH, 1);//um mes dps da data atual
        //setando os valores da interface       
        usu.setIdusuario(idusuario.getText());
        int ide=Integer.parseInt(idexemplar.getText());
        exem.setIdexemplar(ide);
        f.setIdfuncionario(idfuncionario.getText());
        
        DAOLivro2 daol = new DAOLivro2();
        int total=0;
        try {
            if(verificaUsuario(usu)==true && verificalivro(exem,liv)==true){
                //o livro e usuario existem e setamos o idlivro para liv//
                total=liv.getTotexemplares();
                if(total>0){//se tenho exemplares disponiveis//                    
                    
                  //METODO DE CADASTRAR EMPRESTIMO//
                  emp.setIdexemplar(ide);
                  emp.setIdfuncionario(idfuncionario.getText());
                  emp.setDataD(adf.format(cal2.getTime()));
                  emp.setDataE(adf.format(cal.getTime()));
                  emp.setIdusuario(usu.getIdusuario());
                                                      
                  //efetuo o emprestimo
                  DAOEmprestimo2 cad = new DAOEmprestimo2();
                  cad.inserir(emp);
                  
                  //imprimir comprovante de emprestimo//
                  comprovanteEmprestimo(emp);
                                    
                 //ja que o emprestimo foi permitido, atualizo o numero de exemplares disponiveis na entidade LIVRO//
                  daol.updateExemDisp(liv);
                  
                   JOptionPane.showMessageDialog(null, "Emprestimo Permitido!");
                }
                
            }else{
                 JOptionPane.showMessageDialog(null, "Usu√°rio ou Livro n√£o encontrados!");
                
            }} catch (SQLException ex) {
            Logger.getLogger(PermiteEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(PermiteEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
      
        
    }//GEN-LAST:event_jButton3ActionPerformed

