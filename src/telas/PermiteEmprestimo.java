    public boolean verificalivro(Exemplar exe, Livro l){
        DAOExemplar2 daoe = new DAOExemplar2();
        int aux=Integer.parseInt(idexemplar.getText());
        exe.setIdexemplar(aux);
         try {
            if(daoe.getDados(exe)==true){
                l.setIdlivro(exe.getIdlivro());
                
                //pegando a quantidade de exemplares disponiveis//
                DAOLivro2 daol = new DAOLivro2();
                 if(daol.getDados(l)==true){
                   l.setExemplaresdisp(l.getExemplaresdisp());
                 }
                
                return true;
                
            }else{
                JOptionPane.showMessageDialog(null, "Livro n√£o encontrado!");
                return false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(PermiteEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
      return false;
     
    }
    
